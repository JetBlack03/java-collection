    private String dropCourse(String course, String student, String semester){
        
        ArrayList<ScheduleEntry> schedules = ScheduleQueries.getScheduleByStudent(semester, student);
        
        String status = "";
        System.out.print("Course: " + course);
        for(int i = 0; i < schedules.size(); i++){
            System.out.print(schedules.get(i).getCourseCode() );
            
            if (schedules.get(i).getCourseCode().equals(course)){
                status = schedules.get(i).getStatus();
               
            }
        }
        ScheduleQueries.dropStudentScheduleByCourse(semester, student, course);
        if(status.equals("S")){
            System.out.println("waitlist");
            ArrayList<ScheduleEntry> waitlisted = ScheduleQueries.getWaitlistedStudentsByCourse(semester, course);
            if( waitlisted.size()>0){
                StudentEntry student2 =  StudentQueries.getStudent( waitlisted.get(0).getStudentID());
                ScheduleQueries.updateScheduleEntry(semester, waitlisted.get(0));

                return student2.getFirstName() + " " + student2.getLastName()  + " has been added to the class\n";
            }
            else
                return "";
        }else{
            return "";
        }
    }
    

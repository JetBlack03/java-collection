    private void dropStudentButtonAction(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_dropStudentButtonAction
        int studentIndex = dropStudentComboBox.getSelectedIndex();
        String student = StudentQueries.getAllStudents().get(studentIndex).getStudentID();
        StudentEntry entry = StudentQueries.getStudent(student);
        ArrayList<String> semesters = SemesterQueries.getSemesterList();
        
        removeStudentTextArea.setRows(0);
        for (int i = 0; i < semesters.size(); i++){
            removeStudentTextArea.append("\nFor Semester: " + semesters.get(i) + "\n" );
            ArrayList<ScheduleEntry> scheduled = ScheduleQueries.getScheduleByStudent(semesters.get(i), student);
            for(int j = 0; j < scheduled.size(); j++){
                
                removeStudentTextArea.append(entry.getLastName() + ", " +entry.getFirstName()+ " has been dropped from " + scheduled.get(j).getCourseCode() +"\n");
                removeStudentTextArea.append(dropCourse(scheduled.get(j).getCourseCode(), student, semesters.get(i))); 
            }
        }   
        StudentQueries.dropStudent(student);
        removeStudentTextArea.insert(entry.getLastName() + ", " +entry.getFirstName()+ " has been removed from the student list\n\n", 0);
        
        rebuildStudentComboBoxes();
    }//GEN-LAST:event_dropStudentButtonAction

    private void deleteCourseButtonAction(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteCourseButtonAction
        String course = (String)deleteCourseComboBox.getSelectedItem();
        
        ArrayList<ScheduleEntry> scheduled = ScheduleQueries.getScheduledStudentsByCourse(currentSemester, course);
        ArrayList<ScheduleEntry> waitlisted = ScheduleQueries.getWaitlistedStudentsByCourse(currentSemester, course);
                
        CourseQueries.dropCourse(currentSemester, course);
        removeCourseTextArea.setRows(0);
        removeCourseTextArea.append("Scheduled students removed: ");
        for(int i = 0; i < scheduled.size(); i++){
            StudentEntry student = StudentQueries.getStudent(scheduled.get(i).getStudentID());
            removeCourseTextArea.append("\n"+ student.getLastName() + ", " +student.getFirstName());
        }
        
        removeCourseTextArea.append("\n \nWaitlisted students removed: ");
        
        for(int i = 0; i < waitlisted.size(); i++){
            StudentEntry student = StudentQueries.getStudent(waitlisted.get(i).getStudentID());
            removeCourseTextArea.append("\n"+ student.getLastName() + ", " +student.getFirstName());
        }
        
        ScheduleQueries.dropScheduleByCourse(currentSemester, course);
        
        
        
        rebuildCourseComboBoxes(); 
    }//GEN-LAST:event_deleteCourseButtonAction

